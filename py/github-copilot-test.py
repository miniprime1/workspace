# add two numbers
def add(a, b):
    return a + b

# subtract two numbers
def subtract(a, b):
    return a - b

# multiply two numbers
def multiply(a, b):
    return a * b

# divide two numbers
def divide(a, b):
    return a / b

# generate random number between a and b
def random(a, b):
    import random
    return random.randint(a, b)

